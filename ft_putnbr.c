#include "libft.h"

void	ft_putnbr(int n)
{
	unsigned int a;

	if (n < 0)
	{
		n *= -1;
		ft_putchar('-');
	}
	a = n;
	if (a > 9)
	{
		ft_putnbr(a / 10);
		ft_putchar(a % 10 + 48);
	}
	else
		ft_putchar(a + 48);
}
